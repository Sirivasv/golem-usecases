To run the experiment, you need ipython notebook and spearmint package from
https://github.com/inexxt/spearmint
It isa fork which fixes some dependencies issue (branch patch-1)

Then you need to set $SPR_HOME variable in set_pythonpath.sh to the directory in which the spearmint is downloaded and run this script.

The experiment looks for the minimum of `xlog(x)` function.

